// Начальная сумма равна 0
let sum = 0;

// Начинаем бесконечный цикл для постоянного запроса чисел
while (true) {
    let userInput = prompt("Введите число ('0' для окончания):");
    
    // Прервать выполнение цикла, если пользователь нажал 'Отмена'
    if (userInput === null || userInput.trim() === '') break;
    
    // Конвертируем введённую строку в число
    let number = parseFloat(userInput);
    
    // Если пользователь ввёл именно 0, останавливаем цикл
    if (number === 0) break;
    
    // Добавляем введенное число к общей сумме
    sum += number;
}

// Выводим финальную сумму
alert("Сумма введённых чисел: " + sum);