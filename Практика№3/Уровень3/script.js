// Список русских и английских гласных букв
const vowels = ['a', 'e', 'i', 'o', 'u', 'А', 'е', 'ё', 'и', 'о', 'у', 'ы', 'э', 'ю', 'я'];

// Запрашиваем строку у пользователя
let str = prompt("Введите строку:");

// Объявляем счётчик гласных букв
let count = 0;

// Проходим по каждому символу строки
for (let char of str.toLowerCase()) {
    // Проверяем, входит ли текущий символ в список гласных
    if (vowels.includes(char)) {
        count++; // Увеличиваем счётчик, если буква гласная
    }
}

// Выводим результат
alert(`Количество гласных букв: ${count}`);