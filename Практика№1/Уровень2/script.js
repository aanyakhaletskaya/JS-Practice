// Запрашиваем строку у пользователя
let str = prompt("Введите число:");

// Проверяем, является ли введенная строка числом
if (!isNaN(str)) {
    let num = Number(str); // Приводим строку к числу
    
    // Проверяем четность числа
    if (num % 2 === 0) {
        console.log(`Число ${num} — чётное.`);
    } else {
        console.log(`Число ${num} — нечётное.`);
    }
} else {
    console.log("Это не число!");
}